class TwoDArray{

        public static void main(String[] args){
               int arr[][]=new int [2][];
	       arr[0] = new int[]{10, 20, 30}; 
               arr[1] = new int[]{40, 50, 60};

		
                System.out.println(arr[0][0]);
                System.out.println(arr[0][1]);
                System.out.println(arr[1][0]);
                System.out.println(arr[1][2]);

        }
}
